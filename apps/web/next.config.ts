import type { NextConfig } from "next";
// Plugin next-intl désactivé pour éviter les redirections d'URL
// L'internationalisation est gérée côté client via le LanguageContext
// import createNextIntlPlugin from 'next-intl/plugin';

// const withNextIntl = createNextIntlPlugin('./i18n/request.ts');

// Bundle Analyzer - activé uniquement avec ANALYZE=true
const withBundleAnalyzer = require('@next/bundle-analyzer')({
  enabled: process.env.ANALYZE === 'true',
});

const nextConfig: NextConfig = {
  eslint: {
    ignoreDuringBuilds: true,
  },
  typescript: {
    ignoreBuildErrors: true,
  },
  transpilePackages: ['@meeshy/shared'],
  // Configuration Docker - standalone pour optimiser la taille de l'image
  output: 'standalone',

  // Note: URLs are derived dynamically at runtime from window.location in lib/config.ts
  // No publicRuntimeConfig needed - lib/config.ts handles all URL derivation

  poweredByHeader: false,
  
  // Optimisations de performance
  experimental: {
    optimizePackageImports: ['lucide-react', '@radix-ui/react-icons'],
  },
  
  // Configuration Turbopack (stable depuis Next.js 15)
  turbopack: {
    rules: {
      '*.svg': {
        loaders: ['@svgr/webpack'],
        as: '*.js',
      },
    },
  },
  
  // Optimisation des bundles
  modularizeImports: {
    'lucide-react': {
      transform: 'lucide-react/dist/esm/icons/{{kebabCase member}}',
      skipDefaultConversion: true,
    },
  },
  
  
  // Configuration des images
  images: {
    domains: ['localhost', 'meeshy.me', 'gate.meeshy.me', 'ml.meeshy.me'],
    formats: ['image/webp', 'image/avif'],
    deviceSizes: [640, 750, 828, 1080, 1200, 1920, 2048, 3840],
    imageSizes: [16, 32, 48, 64, 96, 128, 256, 384],
  },
  
  // Note: No rewrites for /api - Next.js uses /api for BFF routes
  // Backend access via domain: gate.{domain} (e.g., gate.meeshy.local)
  // Backend access via IP: http://{ip}:3000 directly

  // Pretty share URLs: `/feeds/post/:postId` is the public-facing post URL
  // generated by the gateway and claimed as a Universal Link by the iOS app
  // (see apple-app-site-association). When users without the app follow the
  // link in a browser, Next.js internally serves the existing post detail
  // page that lives under /v2/feeds/post/[postId].
  //
  // `/story/:postId` is the canonical story share URL generated by the iOS
  // app (StoryViewerView, SharePickerView). Stories share the post
  // identifier namespace (a story is a Post with `type: STORY`), so non-iOS
  // recipients land on the same post detail page — the renderer already
  // handles every post variant.
  async rewrites() {
    return [
      {
        source: '/feeds/post/:postId',
        destination: '/v2/feeds/post/:postId',
      },
      {
        source: '/story/:postId',
        destination: '/v2/feeds/post/:postId',
      },
    ];
  },

  // Headers pour PWA et Service Workers
  async headers() {
    return [
      {
        source: '/sw.js',
        headers: [
          {
            key: 'Cache-Control',
            value: 'public, max-age=0, must-revalidate',
          },
          {
            key: 'Service-Worker-Allowed',
            value: '/',
          },
        ],
      },
      {
        source: '/firebase-messaging-sw.js',
        headers: [
          {
            key: 'Cache-Control',
            value: 'public, max-age=0, must-revalidate',
          },
          {
            key: 'Service-Worker-Allowed',
            value: '/',
          },
        ],
      },
      {
        source: '/chunk-recovery.js',
        headers: [
          {
            key: 'Cache-Control',
            value: 'public, max-age=0, must-revalidate',
          },
        ],
      },
      {
        source: '/manifest.json',
        headers: [
          {
            key: 'Cache-Control',
            value: 'public, max-age=604800, immutable',
          },
          {
            key: 'Content-Type',
            value: 'application/manifest+json',
          },
        ],
      },
    ];
  },
  
  
  // Note: No env section needed - lib/config.ts derives URLs dynamically from window.location

  // Configuration WebPack pour Docker
  webpack: (config, { isServer }) => {
    if (!isServer) {
      config.resolve.fallback = {
        ...config.resolve.fallback,
        fs: false,
        net: false,
        tls: false,
      };
    }

    config.resolve.extensionAlias = {
      '.js': ['.ts', '.tsx', '.js', '.jsx'],
      '.mjs': ['.mts', '.mjs'],
    };

    // Exclure dompurify du bundle serveur (client-only)
    if (isServer) {
      config.externals = config.externals || [];
      config.externals.push('dompurify');
    }

    return config;
  },
};

export default withBundleAnalyzer(nextConfig);
